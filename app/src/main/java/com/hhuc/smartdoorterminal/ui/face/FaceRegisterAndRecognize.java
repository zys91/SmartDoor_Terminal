    public boolean checkOrRequestPermission() {
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.M) {
            if (ContextCompat.checkSelfPermission(getActivity(), "android.permission.CAMERA") != PackageManager.PERMISSION_GRANTED
                    || ContextCompat.checkSelfPermission(getActivity(), "android.permission.READ_PHONE_STATE") != PackageManager.PERMISSION_GRANTED) {
                requestPermissions(PERMISSIONS_STORAGE, 101);
                return false;
            }
        }
        return true;
    }

    @Override
    public void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);
        if (requestCode == REQUEST_CODE) {
            try {
                String uid = data.getStringExtra("USERNAME");
                String pwd = data.getStringExtra("USERPASSWORD");
                loginDialog.dismiss();
                Thread aLoginThread = new Thread() {
                    @Override
                    public void run() {
                        super.run();
                        Map<String, String> loginRes = TLogin.loginUser(getActivity(), uid, pwd);
                        Message msg = new Message();
                        if (loginRes != null)
                            if (Objects.equals(loginRes.get("status"), "0")) {
                                msg.what = What_LoginUidErr;
                            } else if (Objects.equals(loginRes.get("status"), "1")) {
                                msg.what = What_LoginSuccess;
                                msg.obj = loginRes;
                            } else if (Objects.equals(loginRes.get("status"), "2")) {
                                msg.what = What_LoginPwdErr;
                            } else
                                msg.what = What_NetworkErr;
                        mHandler.sendMessage(msg);
                    }
                };
                aLoginThread.setDaemon(true);
                aLoginThread.start();
            } catch (Exception er) {
                Log.e(TAG, "acLogin: " + er.getMessage());
            }

        }
    }
